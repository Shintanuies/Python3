print('Nome:', nome)
print('IMC:', imc)
print('-------------------------')
print(nome, 'tem', altura, 'de altura, pesa', peso, 'quilos e seu IMC é', imc)
print('_________________________')
print(nome, 'tem', altura, 'de altura,')
print('pesa', peso, 'quilos e seu IMC é')
